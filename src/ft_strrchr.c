#include "ft_string.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = ft_strlen(s);
	while (i >= 0)
	{
		if (!((s[i] ^ c) & 255))
			return ((char *)s + i);
		i--;
	}
	return (NULL);
}
